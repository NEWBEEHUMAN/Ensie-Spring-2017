    public function removeTrailingSlashAction(Request $request)
    {
        $pathInfo = $request->getPathInfo();
        $requestUri = $request->getRequestUri();

        $url = str_replace($pathInfo, rtrim($pathInfo, ' /'), $requestUri);

        return $this->redirect($url, 301);
    }

    /**
     * @Template()
     */
    public function redirectOldDefinitionAction($word)
    {
        $word = str_replace(' ', '_', $word);
        $word = str_replace('/', '-', $word);
        $word = strtolower($word);
        $redirects = $this->getRedirects();
        if(isset($redirects[$word])){
            $url = $redirects[$word];
            return new RedirectResponse($url, '301');
        }
        //throw new NotFoundHttpException('');
        die('Page not found');
    }

    /**
     * @Template()
     */
    public function redirectOldWriterAction($name)
    {
        $name = str_replace(' ', '_', $name);
        $name = str_replace('/', '-', $name);
        $name = strtolower($name);
        $authorRedirects = $this->getAuthorRedirects();
        if(isset($authorRedirects[$name])){
            $url = $authorRedirects[$name];
            return new RedirectResponse($url, '301');
        }
        throw new NotFoundHttpException('');
    }

    /**
     * @param Request $request
     * @return bool|RedirectResponse
     */
    public function getRedirectResponse(Request $request){
        $requestUri = $request->getRequestUri();
        $redirectUris = $this->getRedirectUris();
        if(isset($redirectUris[$requestUri])){
            return $this->redirect($redirectUris[$requestUri], 301);
        }
        return false;
    }

